    private void LlenarDatos()
    {
        this.IdPue = PuestoControlador.SetdatosromCache(this.txtpuesto_descripcion,this.cmbuesto_estado);
                
        if(IdPue != null)
        {            
            this.btnagregar_puesto.setEnabled(false);
            this.btnmodificar_puesto.setEnabled(true);
            this.btneliminar_puesto.setEnabled(true);
            this.cmbuesto_estado.setEnabled(true);
        }
    }
    private void LimpiarInputs()
    {
        this.txtpuesto_descripcion.setText(null);
  
    } 

